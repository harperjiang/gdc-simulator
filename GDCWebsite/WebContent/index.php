		<div id="part3" class="subtitle">Benefits</div>
		<div class="text">Optimizing compute load shifting in this manner has
			the potential to generate significant efficiencies for the
			utilization of renewable energy sources and data center operations
			and potentially create a new economic model for both renewable energy
			and data centers. Under this new model, the high costs associated
			with transmission infrastructure for renewable energy is replaced by
			relatively low cost and highly efficient data transmission
			infrastructure.</div>
		<div class="text">The main technological benefit of the proposed
			project is the development of a distributed POD network that can be
			powered from renewable energy, primarily wind, potentially consuming
			a significant amount of constrained power that is not otherwise
